package com.maggioni.Whizlab.Pretest.OperatorsAndDecisionConstructs;

public class BitwiseOperatorsDemo1 {

    public static void main(String[] args) {
        int a = 23;
        int b = 42;
        
        boolean c = true;
        boolean d = false;
        System.out.println(" a | b " + (a|b));
        System.out.println(" c | d " + (c|d));
        System.out.println(" c & d " + (c&d));
        
    }

}
